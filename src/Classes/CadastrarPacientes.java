    public void readJTablePacientes(){
        DefaultTableModel modelo = (DefaultTableModel) jTablePacientes.getModel();
       modelo.setNumRows(0);
        ControlePaciente dao = new ControlePaciente();
        
        for(Paciente P: dao.read()){
            
            modelo.addRow(new Object[]{
            
                P.getNumeroLivroMedico(),
                P.getNome(),
                P.getBi(),
                P.getDataNascimento(),
                P.getSexo(),
                
            
            });
        }
    
    }
